UnicodeXidStartFlagData::UnicodeXidStartFlagData()
{
    // initialize all flags to 0
    for(size_t i = 0; i < this->flags.size(); i++) {
        this->flags[i] = false;
    }

    // now set all of the bits corresponding to the code points in the range
    WorkParallelForEach(xidStartRangeData->ranges.begin(), xidStartRangeData->ranges.end(),
        [this](const std::pair<uint32_t, uint32_t>& entry) {
            for(uint32_t i = entry.first; i <= entry.second; i++) {
                this->flags[static_cast<size_t>(i)] = true;
            }
        });
}

